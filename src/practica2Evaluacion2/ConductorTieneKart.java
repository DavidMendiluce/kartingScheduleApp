package practica2Evaluacion2;

public class ConductorTieneKart {
  String conductor;
  String kart;
  
  public ConductorTieneKart(String conductor, String kart) {
	  this.conductor=conductor;
	  this.kart=kart;
	  
  }
  
  public String getConductor() {
	  return this.conductor;
  }
  
  public String getKart() {
	  return this.kart;
  }
}
